

public class _08Switch{
	public static void main(String args[]){


		int number = 2;

		switch(number) {

		case 1 : System.out.println("your given number is 1:");	break;
		case 2 : System.out.println("your given number is 2:"); break;
		case 3 : System.out.println("your given number is 3:"); break;
		case 4 : System.out.println("your given number is 4:"); break;
		default: System.out.println("Default number.");
		}
	}
}